public class OrderReprository {

    public boolean orderToSent (Supplier supplier , ProductList productList){
         if(supplier.equals(supplier) && productList.getListOfProducts().equals(productList.getListOfProducts())){
             System.out.println("Order is ready to sent ");
             return true;
         }else {
             System.out.println("Order is not ready to sent ");
             return false;
         }

    }
}
